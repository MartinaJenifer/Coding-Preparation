//difference between let var and const

// Using let
let age = 25;
age = 30;
console.log("Age (let):", age); // Output: 30

// Using var
var name = "John";
name = "Jane";
console.log("Name (var):", name); // Output: Jane

// Using const
const pi = 3.14;
// pi = 3.14159; // Uncommenting this line will throw an error
console.log("Pi (const):", pi); // Output: 3.14
